package week10;

public class Mid_2 {
	
	private String title, artist, country;
	private int year;
	
	

	public Mid_2(String title, String artist, String country, int year) {
		super();
		this.title = title;
		this.artist = artist;
		this.country = country;
		this.year = year;
	}

	public String getTitle() {
		return title;
	}

	public String getArtist() {
		return artist;
	}

	public String getCountry() {
		return country;
	}

	public int getYear() {
		return year;
	}
	
	

}
